#include "fillit.h"

void	ft_print(char **res)
{
	int x;
	int y;

	y = -1;
	while (res[++y] != NULL)
	{
		x = -1;
		while (res[y][++x])
			write(1, &res[y][x], 1);
		write(1, "\n", 1);
	}
}
